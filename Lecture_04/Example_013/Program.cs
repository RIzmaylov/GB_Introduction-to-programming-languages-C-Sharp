void PrintArray(int[,] matrix)
{
    for (int rows = 0; rows < matrix.GetLength(0); ++rows)
    {
        for (int cols = 0; cols < matrix.GetLength(1); ++cols)
        {
            Console.Write($"{matrix[rows, cols]} ");
        }
        Console.WriteLine();
    }
}

void FillArray(int[,] matrix)
{
    for (int rows = 0; rows < matrix.GetLength(0); ++rows)
    {
        for (int cols = 0; cols < matrix.GetLength(1); ++cols)
        {
            matrix[rows, cols] = new Random().Next(1, 10);
        }
    }
}

int[,] matrix = new int[3, 4];

PrintArray(matrix);
FillArray(matrix);
Console.WriteLine();
PrintArray(matrix);

void PrintImage(int[,] pic)
{
    for (int rows = 0; rows < pic.GetLength(0); ++rows)
    {
        for (int cols = 0; cols < pic.GetLength(1); ++cols)
        {
            if (pic[rows, cols] == 1)
                Console.Write("+");
            else
                Console.Write(" ");
        }
        Console.WriteLine();
    }
}

